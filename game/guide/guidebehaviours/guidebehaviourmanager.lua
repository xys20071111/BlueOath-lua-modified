function GuideBehaviourManager:buildBehaviour(nType, objHolder, objParam)
  local objRequired = self.tblBehaviourHand[nType]
  if objRequired == nil then
    logError("cant find behaviour type " .. tostring(nType))
    return
  end
  local objResult = objRequired:new(nType, objHolder, objParam)
  return objResult
end

return GuideBehaviourManager
